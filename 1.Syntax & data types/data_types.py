# Integer
a = 10
print("a is an integer: {a}")

# Float
b = 20.5
print("b is a float: {b}")

# String
c = "Hello, Python!"
print("c is a string: {c}")

# Boolean
d = True
print("d is a boolean: {d}")

# List
e = [1, 2, 3, 4, 5]
print("e is a list: {e}")

# Dictionary
f = {"name": "Lohit", "age": 30}
print("f is a dictionary: {f}")

# Tuple
g = (1,2,3)
print("g is a tuple: {g}")